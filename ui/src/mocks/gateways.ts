/** All gateway links. Filtered per node in mockGatewayDetail. */
const ALL_GATEWAY_LINKS = [
  { gateway_link_id: 'gl-a-b', source_node_id: 'node-a', target_node_id: 'node-b', status: 'active' as const },
];

export function mockGatewayDetail(id: string): GatewayDetail | null {
  const gw = mockGateways.find((g) => g.gateway_id === id);
  if (!gw) return null;
  return {
    ...gw,
    routes_served: id === 'gw_public_b' ? 3 : 1,
    /** Show links where this node is source or target. */
    gateway_links: ALL_GATEWAY_LINKS.filter(
      (l) => l.source_node_id === gw.node_id || l.target_node_id === gw.node_id,
    ),
  };
}
